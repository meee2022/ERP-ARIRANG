/**

 * Builds the journal lines for posting a purchase invoice.

 * Accounting rules:

 *   DR Inventory / Expense accounts  (per line)

 *   DR VAT Receivable                (if VAT > 0)

 *   CR Accounts Payable              (supplier)

 */

export function buildPurchaseInvoiceJournal(

  invoice: PurchaseInvoiceForJournal,

  lines: PurchaseInvoiceLineForJournal[],

  apAccountId: Id<"accounts">,

  vatReceivableAccountId: Id<"accounts">

): JournalLineInput[] {

  const journalLines: JournalLineInput[] = [];



  // DR each line account

  for (const line of lines) {

    if (line.accountId) {

      journalLines.push({

        accountId: line.accountId,

        description: `مشتريات - ${invoice.invoiceNumber}`,

        debit: line.lineTotal - line.vatAmount,

        credit: 0,

        costCenterId: line.costCenterId,

      });

    }

  }



  // DR VAT Receivable

  if (invoice.vatAmount > 0) {

    journalLines.push({

      accountId: vatReceivableAccountId,

      description: `ضريبة مدخلات - ${invoice.invoiceNumber}`,

      debit: invoice.vatAmount,

      credit: 0,

    });

  }



  // CR Accounts Payable

  journalLines.push({

    accountId: apAccountId,

    subAccountType: "supplier",

    subAccountId: invoice.supplierId,

    description: `ذمم مورد - ${invoice.invoiceNumber}`,

    debit: 0,

    credit: invoice.totalAmount,

  });



  return journalLines;

}



