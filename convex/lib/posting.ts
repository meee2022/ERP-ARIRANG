/**

 * Builds the journal lines for posting a sales invoice.

 * Accounting rules:

 *   DR Cash/Card/AR      (for each payment method)

 *   CR Revenue accounts  (per line)

 *   CR VAT Payable       (if vatAmount > 0)

 *   DR COGS              (per line)

 *   CR Inventory         (per line)

 */

export function buildSalesInvoiceJournal(

  invoice: SalesInvoiceForJournal,

  lines: SalesInvoiceLineForJournal[],

  cashAccountId: Id<"accounts">,

  cardAccountId: Id<"accounts">,

  arAccountId: Id<"accounts">,

  vatPayableAccountId: Id<"accounts">

): JournalLineInput[] {

  const journalLines: JournalLineInput[] = [];



  // DR Cash received

  if (invoice.cashReceived > 0) {

    journalLines.push({

      accountId: cashAccountId,

      description: `نقد - ${invoice.invoiceNumber}`,

      debit: invoice.cashReceived,

      credit: 0,

      costCenterId: invoice.costCenterId,

    });

  }



  // DR Card received

  if (invoice.cardReceived > 0) {

    journalLines.push({

      accountId: cardAccountId,

      description: `بطاقة - ${invoice.invoiceNumber}`,

      debit: invoice.cardReceived,

      credit: 0,

      costCenterId: invoice.costCenterId,

    });

  }



  // DR AR (credit amount)

  if (invoice.creditAmount > 0 && invoice.customerId) {

    journalLines.push({

      accountId: arAccountId,

      subAccountType: "customer",

      subAccountId: invoice.customerId,

      description: `ذمم عميل - ${invoice.invoiceNumber}`,

      debit: invoice.creditAmount,

      credit: 0,

      costCenterId: invoice.costCenterId,

    });

  }



  // CR Revenue per line

  for (const line of lines) {

    if (line.revenueAccountId && line.lineTotal > 0) {

      journalLines.push({

        accountId: line.revenueAccountId,

        description: `إيراد - ${invoice.invoiceNumber}`,

        debit: 0,

        credit: line.lineTotal - line.vatAmount,

        costCenterId: line.costCenterId ?? invoice.costCenterId,

      });

    }

  }



  // CR VAT Payable

  if (invoice.vatAmount > 0) {

    journalLines.push({

      accountId: vatPayableAccountId,

      description: `ضريبة قيمة مضافة - ${invoice.invoiceNumber}`,

      debit: 0,

      credit: invoice.vatAmount,

    });

  }



  // DR COGS / CR Inventory per line (for stock items)

  for (const line of lines) {

    if (line.cogsAccountId && line.costTotal > 0) {

      journalLines.push({

        accountId: line.cogsAccountId,

        description: `تكلفة البضاعة المباعة - ${invoice.invoiceNumber}`,

        debit: line.costTotal,

        credit: 0,

        costCenterId: line.costCenterId ?? invoice.costCenterId,

      });

    }

  }



  return journalLines;

}



