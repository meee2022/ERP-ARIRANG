export const createCashReceiptVoucher = mutation({
  args: {
    companyId: v.id("companies"),
    branchId: v.id("branches"),
    voucherDate: v.string(),
    periodId: v.id("accountingPeriods"),
    receivedFrom: v.string(),
    customerId: v.optional(v.id("customers")),
    receiptType: v.union(
      v.literal("customer_payment"),
      v.literal("other_receipt")
    ),
    cashAccountId: v.id("accounts"),
    amount: v.number(),
    currencyId: v.id("currencies"),
    exchangeRate: v.number(),
    paymentMethod: v.union(
      v.literal("cash"),
      v.literal("transfer"),
      v.literal("card"),
      v.literal("cheque")
    ),
    reference: v.optional(v.string()),
    costCenterId: v.optional(v.id("costCenters")),
    forMonth: v.optional(v.string()),
    notes: v.optional(v.string()),
    createdBy: v.id("users"),
  },
  handler: async (ctx, args) => {
    // Permission check before any DB writes
    const user = await assertUserPermission(ctx, args.createdBy, "treasury", "create");
    assertUserBranch(user, args.branchId);

    await validatePeriodOpen(ctx, args.periodId);
    await assertPeriodOpen(ctx, args.voucherDate, args.companyId);

    const fiscalYear = await ctx.db
      .query("fiscalYears")
      .withIndex("by_company", (q) => q.eq("companyId", args.companyId))
      .filter((q) => q.eq(q.field("status"), "open"))
      .first();

    let voucherNumber = `CRV-${Date.now()}`;
    if (fiscalYear) {
      voucherNumber = await generateDocumentNumber(ctx, args.branchId, fiscalYear._id, "CRV");
    }

    const voucherId = await ctx.db.insert("cashReceiptVouchers", {
      companyId: args.companyId,
      branchId: args.branchId,
      voucherNumber,
      voucherDate: args.voucherDate,
      periodId: args.periodId,
      receivedFrom: args.receivedFrom,
      customerId: args.customerId,
      receiptType: args.receiptType,
      cashAccountId: args.cashAccountId,
      amount: args.amount,
      currencyId: args.currencyId,
      exchangeRate: args.exchangeRate,
      paymentMethod: args.paymentMethod,
      reference: args.reference,
      costCenterId: args.costCenterId,
      forMonth: args.forMonth,
      documentStatus: "draft" as const,
      postingStatus: "unposted" as const,
      allocationStatus: "unallocated" as const,
      notes: args.notes,
      createdBy: args.createdBy,
      createdAt: Date.now(),
    });

    await logAudit(ctx, {
      companyId: args.companyId,
      userId: args.createdBy,
      action: "create",
      module: "treasury",
      documentType: "cashReceiptVoucher",
      documentId: String(voucherId),
    });

    return { voucherId, voucherNumber };
  },
});

export const postCashReceiptVoucher = mutation({
  args: {
    voucherId: v.id("cashReceiptVouchers"),
    userId: v.id("users"),
    arAccountId: v.id("accounts"),
  },
  handler: async (ctx, args) => {
    const voucher = await ctx.db.get(args.voucherId);
    if (!voucher) throw new Error("سند القبض غير موجود");
    if (voucher.postingStatus === "posted") throw new Error("السند مرحل مسبقاً");

    await validatePeriodOpen(ctx, voucher.periodId);

    const journalLines = buildCashReceiptJournal(
      {
        _id: voucher._id,
        voucherNumber: voucher.voucherNumber,
        cashAccountId: voucher.cashAccountId,
        amount: voucher.amount,
        customerId: voucher.customerId,
        receiptType: voucher.receiptType,
      },
      args.arAccountId
    );

    const journalEntryId = await postJournalEntry(
      ctx,
      {
        companyId: voucher.companyId,
        branchId: voucher.branchId,
        journalType: "auto_receipt",
        entryDate: voucher.voucherDate,
        periodId: voucher.periodId,
        currencyId: voucher.currencyId,
        exchangeRate: voucher.exchangeRate,
        sourceType: "cashReceiptVoucher",
        sourceId: voucher._id,
        description: `Cash Receipt / سند قبض - ${voucher.voucherNumber}`,
        isAutoGenerated: true,
        createdBy: args.userId,
      },
      journalLines
    );

    await ctx.db.patch(args.voucherId, {
      postingStatus: "posted",
      documentStatus: "approved",
      journalEntryId,
    });

    return { success: true, journalEntryId };
  },
});

export const allocateReceipt = mutation({
  args: {
    voucherId: v.id("cashReceiptVouchers"),
    userId: v.id("users"),
    allocations: v.array(
      v.object({
        invoiceId: v.id("salesInvoices"),
        amount: v.number(),
      })
    ),
  },
  handler: async (ctx, args) => {
    const voucher = await ctx.db.get(args.voucherId);
    if (!voucher) throw new Error("سند القبض غير موجود");
    if (voucher.postingStatus !== "posted") {
      throw new Error("يجب ترحيل سند القبض أولاً");
    }

    // Check existing allocations
    const existingAllocations = await ctx.db
      .query("receiptAllocations")
      .withIndex("by_voucher", (q) => q.eq("voucherId", args.voucherId))
      .filter((q) => q.eq(q.field("isReversed"), false))
      .collect();

    const alreadyAllocated = existingAllocations.reduce(
      (s, a) => s + a.allocatedAmount,
      0
    );
    const newAllocationTotal = args.allocations.reduce((s, a) => s + a.amount, 0);

    if (alreadyAllocated + newAllocationTotal > voucher.amount) {
      throw new Error(
        `المبلغ المخصص (${(newAllocationTotal / 100).toFixed(2)}) يتجاوز الرصيد المتاح في السند`
      );
    }

    const today = new Date().toISOString().split("T")[0];

    for (const alloc of args.allocations) {
      const invoice = await ctx.db.get(alloc.invoiceId);
      if (!invoice) throw new Error(`الفاتورة ${alloc.invoiceId} غير موجودة`);

      await ctx.db.insert("receiptAllocations", {
        voucherId: args.voucherId,
        invoiceId: alloc.invoiceId,
        allocatedAmount: alloc.amount,
        allocationDate: today,
        createdBy: args.userId,
        createdAt: Date.now(),
        isReversed: false,
      });

      // Update invoice payment status
      const allInvoiceAllocations = await ctx.db
        .query("receiptAllocations")
        .withIndex("by_invoice", (q) => q.eq("invoiceId", alloc.invoiceId))
        .filter((q) => q.eq(q.field("isReversed"), false))
        .collect();

      const totalAllocated =
        allInvoiceAllocations.reduce((s, a) => s + a.allocatedAmount, 0) +
        alloc.amount;

      let paymentStatus: "unpaid" | "partial" | "paid" = "unpaid";
      if (totalAllocated >= invoice.totalAmount) {
        paymentStatus = "paid";
      } else if (totalAllocated > 0) {
        paymentStatus = "partial";
      }

      await ctx.db.patch(alloc.invoiceId, { paymentStatus });
    }

    // Update voucher allocation status
    const totalAllocatedNow = alreadyAllocated + newAllocationTotal;
    let allocationStatus: "unallocated" | "partial" | "fully_allocated" =
      "unallocated";
    if (totalAllocatedNow >= voucher.amount) {
      allocationStatus = "fully_allocated";
    } else if (totalAllocatedNow > 0) {
      allocationStatus = "partial";
    }

    await ctx.db.patch(args.voucherId, { allocationStatus });

    return { success: true };
  },
});

