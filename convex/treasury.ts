export const reverseCashReceiptVoucher = mutation({
  args: {
    voucherId: v.id("cashReceiptVouchers"),
    userId: v.id("users"),
    reversalDate: v.string(),
    reversalPeriodId: v.id("accountingPeriods"),
  },
  handler: async (ctx, args) => {
    const voucher = await ctx.db.get(args.voucherId);
    if (!voucher) throw new Error("سند القبض غير موجود");
    if (voucher.postingStatus !== "posted") {
      throw new Error("يمكن عكس سندات القبض المرحلة فقط");
    }
    if (!voucher.journalEntryId) {
      throw new Error("لا يوجد قيد محاسبي مرتبط بسند القبض");
    }

    const allocations = await ctx.db
      .query("receiptAllocations")
      .withIndex("by_voucher_active", (q) => q.eq("voucherId", args.voucherId).eq("isReversed", false))
      .collect();
    if (allocations.length > 0) {
      throw new Error("لا يمكن عكس سند قبض عليه تخصيصات. قم بعكس التخصيصات أولاً");
    }

    await reverseJournalEntry(ctx, voucher.journalEntryId, args.reversalDate, args.reversalPeriodId, args.userId);
    await ctx.db.patch(args.voucherId, { postingStatus: "reversed" });
    return { success: true };
  },
});

