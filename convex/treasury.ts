export const updateChequeStatus = mutation({
  args: {
    chequeId: v.id("cheques"),
    newStatus: v.union(
      v.literal("deposited"),
      v.literal("cleared"),
      v.literal("bounced"),
      v.literal("presented"),
      v.literal("cleared_issued"),
      v.literal("stopped")
    ),
    date: v.string(),
    userId: v.id("users"),
    periodId: v.optional(v.id("accountingPeriods")),
    bankAccountId: v.optional(v.id("accounts")),
    chequesInTransitAccountId: v.optional(v.id("accounts")),
    bounceChargesAccountId: v.optional(v.id("accounts")),
    bounceReason: v.optional(v.string()),
    notes: v.optional(v.string()),
  },
  handler: async (ctx, args) => {
    const cheque = await ctx.db.get(args.chequeId);
    if (!cheque) throw new Error("الشيك غير موجود");

    // Permission check before any updates
    const chequeStatusUser = await assertUserPermission(ctx, args.userId, "treasury", "edit");
    assertUserBranch(chequeStatusUser, cheque.branchId);

    const updates: Record<string, any> = {
      chequeStatus: args.newStatus,
      notes: args.notes ?? cheque.notes,
    };

    if (args.newStatus === "deposited") {
      updates.depositDate = args.date;
    }
    if (args.newStatus === "cleared" || args.newStatus === "cleared_issued") {
      updates.clearingDate = args.date;

      // Create clearing journal entry if accounts provided
      if (args.periodId && args.bankAccountId && args.chequesInTransitAccountId) {
        const journalEntryId = await postJournalEntry(
          ctx,
          {
            companyId: cheque.companyId,
            branchId: cheque.branchId,
            journalType: "auto_cheque",
            entryDate: args.date,
            periodId: args.periodId,
            currencyId: cheque.currencyId,
            exchangeRate: cheque.exchangeRate,
            sourceType: "cheque",
            sourceId: cheque._id,
            description: `تسوية شيك رقم ${cheque.chequeNumber}`,
            isAutoGenerated: true,
            createdBy: args.userId,
          },
          cheque.chequeType === "received"
            ? [
                {
                  accountId: args.bankAccountId,
                  description: `تحصيل شيك ${cheque.chequeNumber}`,
                  debit: cheque.amount,
                  credit: 0,
                },
                {
                  accountId: args.chequesInTransitAccountId,
                  description: `تحصيل شيك ${cheque.chequeNumber}`,
                  debit: 0,
                  credit: cheque.amount,
                },
              ]
            : [
                {
                  accountId: args.chequesInTransitAccountId,
                  description: `صرف شيك ${cheque.chequeNumber}`,
                  debit: cheque.amount,
                  credit: 0,
                },
                {
                  accountId: args.bankAccountId,
                  description: `صرف شيك ${cheque.chequeNumber}`,
                  debit: 0,
                  credit: cheque.amount,
                },
              ]
        );
        updates.clearingEntryId = journalEntryId;
      }
    }
    if (args.newStatus === "bounced") {
      updates.bounceDate = args.date;
      updates.bounceReason = args.bounceReason;
    }
    if (args.newStatus === "stopped") {
      // No additional journal needed
    }

    await ctx.db.patch(args.chequeId, updates);

    await logAudit(ctx, {
      companyId: cheque.companyId,
      userId: args.userId,
      action: "edit",
      module: "treasury",
      documentType: "cheque",
      documentId: String(args.chequeId),
      details: JSON.stringify({ newStatus: args.newStatus }),
    });

    return { success: true };
  },
});

// ─── BANK TRANSFERS ───────────────────────────────────────────────────────────

export const createBankTransfer = mutation({
  args: {
    companyId: v.id("companies"),
    branchId: v.id("branches"),
    transferDate: v.string(),
    periodId: v.id("accountingPeriods"),
    fromBankAccountId: v.id("bankAccounts"),
    toBankAccountId: v.id("bankAccounts"),
    amount: v.number(),
    currencyId: v.id("currencies"),
    reference: v.optional(v.string()),
    description: v.optional(v.string()),
    createdBy: v.id("users"),
  },
  handler: async (ctx, args) => {
    // Permission check before any processing
    const transferUser = await assertUserPermission(ctx, args.createdBy, "treasury", "create");
    assertUserBranch(transferUser, args.branchId);

    // 1. Validate accounts are different
    if (args.fromBankAccountId === args.toBankAccountId) {
      throw new Error("لا يمكن التحويل من وإلى نفس الحساب البنكي");
    }

    await validatePeriodOpen(ctx, args.periodId);
    await assertPeriodOpen(ctx, args.transferDate, args.companyId);

    // 2. Look up both bank accounts to get their glAccountId
    const fromBankAccount = await ctx.db.get(args.fromBankAccountId);
    if (!fromBankAccount) throw new Error("حساب البنك المُحوَّل منه غير موجود");
    if (!fromBankAccount.glAccountId) throw new Error("حساب البنك المُحوَّل منه لا يرتبط بحساب محاسبي");

    const toBankAccount = await ctx.db.get(args.toBankAccountId);
    if (!toBankAccount) throw new Error("حساب البنك المُحوَّل إليه غير موجود");
    if (!toBankAccount.glAccountId) throw new Error("حساب البنك المُحوَّل إليه لا يرتبط بحساب محاسبي");

    // 3. Generate transfer number
    const fiscalYear = await ctx.db
      .query("fiscalYears")
      .withIndex("by_company", (q) => q.eq("companyId", args.companyId))
      .filter((q) => q.eq(q.field("status"), "open"))
      .first();

    let transferNumber = `TRF-${Date.now()}`;
    if (fiscalYear) {
      transferNumber = await generateDocumentNumber(ctx, args.branchId, fiscalYear._id, "TRF");
    }

    // 4. Build journal lines:
    //    DEBIT: toBankAccount.glAccountId  (money going IN)
    //    CREDIT: fromBankAccount.glAccountId (money going OUT)
    const journalLines = [
      {
        accountId: toBankAccount.glAccountId,
        description: `تحويل بنكي ${transferNumber} - وارد`,
        debit: args.amount,
        credit: 0,
      },
      {
        accountId: fromBankAccount.glAccountId,
        description: `تحويل بنكي ${transferNumber} - صادر`,
        debit: 0,
        credit: args.amount,
      },
    ];

    // 5. Create the journal entry (posts immediately)
    const journalEntryId = await postJournalEntry(
      ctx,
      {
        companyId: args.companyId,
        branchId: args.branchId,
        journalType: "auto_transfer",
        entryDate: args.transferDate,
        periodId: args.periodId,
        currencyId: args.currencyId,
        exchangeRate: 1,
        sourceType: "bankTransfer",
        sourceId: transferNumber,
        description: args.description
          ? `تحويل بنكي ${transferNumber} - ${args.description}`
          : `تحويل بنكي ${transferNumber}`,
        isAutoGenerated: true,
        createdBy: args.createdBy,
      },
      journalLines
    );

    // 6. Insert bankTransfer record — posted immediately
    const transferId = await ctx.db.insert("bankTransfers", {
      companyId: args.companyId,
      branchId: args.branchId,
      transferNumber,
      transferDate: args.transferDate,
      periodId: args.periodId,
      fromAccountId: args.fromBankAccountId,
      toAccountId: args.toBankAccountId,
      amount: args.amount,
      feeAmount: 0,
      currencyId: args.currencyId,
      reference: args.reference,
      documentStatus: "approved",
      postingStatus: "posted",
      journalEntryId,
      notes: args.description,
      createdBy: args.createdBy,
      createdAt: Date.now(),
    });

    await logAudit(ctx, {
      companyId: args.companyId,
      userId: args.createdBy,
      action: "create",
      module: "treasury",
      documentType: "bankTransfer",
      documentId: String(transferId),
    });

    return { transferId, transferNumber };
  },
});

