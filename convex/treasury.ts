export const listReceipts = query({
  args: {
    companyId: v.id("companies"),
    branchId: v.optional(v.id("branches")),
    fromDate: v.optional(v.string()),
    toDate: v.optional(v.string()),
    postingStatus: v.optional(
      v.union(v.literal("unposted"), v.literal("posted"), v.literal("reversed"))
    ),
    allocationStatus: v.optional(
      v.union(v.literal("unallocated"), v.literal("partial"), v.literal("fully_allocated"))
    ),
  },
  handler: async (ctx, args) => {
    const all = await ctx.db
      .query("cashReceiptVouchers")
      .filter((q) => q.eq(q.field("companyId"), args.companyId))
      .order("desc")
      .take(200);

    const filtered = all.filter((r) => {
      if (args.branchId && r.branchId !== args.branchId) return false;
      if (args.fromDate && r.voucherDate < args.fromDate) return false;
      if (args.toDate && r.voucherDate > args.toDate) return false;
      if (args.postingStatus && r.postingStatus !== args.postingStatus) return false;
      if (args.allocationStatus && r.allocationStatus !== args.allocationStatus) return false;
      return true;
    });

    return Promise.all(
      filtered.map(async (r) => {
        const customer = r.customerId ? await ctx.db.get(r.customerId) : null;
        return { ...r, customerName: customer?.nameAr ?? null };
      })
    );
  },
});

